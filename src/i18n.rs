pub fn hotkey_label(language: &Language, hotkey: &Hotkey) -> String {
    match language {
        Language::ZhCn => match hotkey {
            Hotkey::RightCtrl => "右 Ctrl".to_string(),
            Hotkey::CapsLock => "CapsLock".to_string(),
            Hotkey::RightAlt => "右 Alt".to_string(),
            Hotkey::CtrlSpace => "Ctrl+Space".to_string(),
            Hotkey::FnScanCode { scan_code } => format!("Fn 扫描码 0x{scan_code:x}"),
            Hotkey::Custom { .. } => hotkey.display_name(),
        },
        Language::ZhTw => match hotkey {
            Hotkey::RightCtrl => "右 Ctrl".to_string(),
            Hotkey::CapsLock => "CapsLock".to_string(),
            Hotkey::RightAlt => "右 Alt".to_string(),
            Hotkey::CtrlSpace => "Ctrl+Space".to_string(),
            Hotkey::FnScanCode { scan_code } => format!("Fn 掃描碼 0x{scan_code:x}"),
            Hotkey::Custom { .. } => hotkey.display_name(),
        },
        _ => hotkey.display_name(),
    }
}

pub fn stt_compat_label(language: &Language, compatibility: &SttCompatibility) -> &'static str {
    match (language, compatibility) {
        (Language::ZhCn, SttCompatibility::OpenAiAudioTranscriptions) => "OpenAI 音频转录接口",
        (Language::ZhCn, SttCompatibility::AliyunQwenAsrChat) => "阿里云 Qwen-ASR 兼容模式",
        (Language::ZhTw, SttCompatibility::OpenAiAudioTranscriptions) => "OpenAI 音訊轉錄介面",
        (Language::ZhTw, SttCompatibility::AliyunQwenAsrChat) => "阿里雲 Qwen-ASR 相容模式",
        _ => compatibility.display_name(),
    }
}

